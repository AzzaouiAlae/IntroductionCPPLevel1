#include <iostream>

using namespace std;

int main()
{
    short A, B;
    cout << "Plese enter the first number A : ";
    cin >> A;
    cout << "Plese enter the second number B : ";
    cin >> B;

    cout << A << " + " << B << " = " << A + B << endl;
    cout << A << " - " << B << " = " << A - B << endl;
    cout << A << " * " << B << " = " << A * B << endl;
    cout << A << " / " << B << " = " << A / B << endl;
    cout << A << " % " << B << " = " << A % B << endl;
}
